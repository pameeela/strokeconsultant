<h2>DIRECTIONS</h2>

<p><strong>From Merritt/Hutchinson Parkway</strong></p>

<p>Take Merritt Parkway Exit 31 marked North Street/Greenwich and follow signs for Greenwich Business District heading south (away from Bedford). Continue on North Street for about 2 miles.</p>
<p>Greenwich Catholic School is located at 471 North Street on the right behind St. Michael's Church at the corner of North Street and Clapboard Ridge Road.</p>

<p><strong>From I-95/Connecticut Turnpike</strong></p>

<p>Take Exit 4 marked Greenwich/Indian Field Road. From the south, take a left onto Indian Field Road; from the north, take a right. Follow Indian Field Road straight until light at intersection with Post Road. Go straight through intersection, follow curve in road, and take first left, Stanwich Road. Continue on Stanwich Road and take left onto Fairfield Avenue which curves around playing fields and goes under a pedestrian bridge. Follow up a hill and curve past Greenwich Country Day School entrance on your left, and bear right past intersection and triangle onto North Street. Continue on North Street for approximately 1 ½ miles and Greenwich Catholic School will be located on your left side after you see our soccer field. Take the second entrance (the first is an exit) by St. Michael Rectory and Church (modern wood church) and continue into parking area.</p>

<p>The gymnasium/pool is up a hill to the left of the main school complex as you face it.</p>

